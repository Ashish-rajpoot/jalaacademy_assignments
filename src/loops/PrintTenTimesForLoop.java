package loops;

public class PrintTenTimesForLoop {

//    Write a program to print “Bright IT Career” ten times using for loop
public static void main(String[] args) {
    for (int i = 0; i < 10; i++) {
        System.out.println(( i + 1) + ": Bright IT career");
    }
}
}
