package basics;

public class DataTypes {

    int number = 1;
    String name = "Jala";
    boolean isAvailable = true;
    float price = 100.54f;
    char alphabet = 'a';


    public static void main(String[] args) {
        DataTypes dataTypes = new DataTypes();
        System.out.println(dataTypes.number);
        System.out.println(dataTypes.name);
        System.out.println(dataTypes.isAvailable);
        System.out.println(dataTypes.price);
        System.out.println(dataTypes.alphabet);
    }
}
